package logic.presentation.bean;

public class CandidateBean {
	
	private int offer;
	private String name;
	private Long seeker;
	
	public int getOffer() {
		return offer;
	}

	public void setOffer(int offer) {
		this.offer = offer;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getSeeker() {
		return seeker;
	}

	public void setSeeker(Long seeker) {
		this.seeker = seeker;
	}
}
